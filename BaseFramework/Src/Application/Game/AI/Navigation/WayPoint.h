class WayPoint
{
public:

	// コンストラクタ
	WayPoint();

	//--------------------------------------------------
	// 所得
	//--------------------------------------------------
	// 座標所得
	const KdVector3 GetPosition() const { return m_position; }

	//--------------------------------------------------
	// 設定
	//--------------------------------------------------
	// 座標設定
	void SetPosition(const KdVector3 position) { m_position = position; }

private:
	KdVector3 m_position;
};